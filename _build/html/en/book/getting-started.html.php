<h2>Downloading PPI<a class="headerlink" href="#downloading-ppi" title="Permalink to this headline">¶</a></h2>
<p>You can download the ppi skeletonaapp from the Homepage. If you just want everything in one folder ready to go, you should choose the <strong>&#8220;ppi skeletonapp with vendors&#8221;</strong> option.</p>
<p>If you are comfortable with using <strong>git</strong> then you can download the <strong>&#8220;skeleton app without vendors&#8221;</strong> option and run the following commands:</p>
<div class="highlight-bash"><div class="highlight"><pre>curl -s http://getcomposer.org/installer | php
php composer.phar install
</pre></div>
</div>
</div>
<div class="section" id="system-requirements">
<h2>System requirements<a class="headerlink" href="#system-requirements" title="Permalink to this headline">¶</a></h2>
<p>A web server with its rewrite module enabled. (mod_rewrite for apache)</p>
<p>PPI needs <strong>5.3.3</strong> or above. The recommended version by symfony is <strong>5.3.10</strong> or above.</p>
