<h1>PPI Framework<a class="headerlink" href="#ppi-framework" title="Permalink to this headline">¶</a></h1>
<p><a class="reference external" href="http://ppi.io/">PPI</a> is an open source <a class="reference external" href="http://php.net/">PHP</a> meta-framework.</p>
<p>We have taken the good bits from <a class="reference external" href="http://symfony.com/">Symfony2</a>, <a class="reference external" href="http://framework.zend.com/">ZendFramework2</a> &amp; <a class="reference external" href="http://www.doctrine-project.org/">Doctrine2</a> and combined them together to create a solid and very easy web application framework. PPI can be considered the boilerplate of PHP frameworks.</p>
